package devskill.school.utility;

public class Calcolatrice {
    private Integer a;
    private Integer b;


    public Calcolatrice() {

    }

    public Integer addizione(int a, int b) {
        return a + b;
    }
    public Integer sottrazione(int a, int b) {
        return a - b;
    }
    public Integer moltiplicazione(int a, int b) {
        return a * b;
    }
    public Integer divisione(int a, int b) {
        return a / b;
    }
}